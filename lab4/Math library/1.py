import math

degree = float(input("Input degree: "))
radian = math.radians(degree) 

print("Output radian:", round(radian, 6))


''' IMPORT MATH 
math — это стандартный модуль Python с математическими функциями:

math.pi — число π (3.14159…)

math.sin(x), math.cos(x), math.tan(x) — тригонометрия

math.sqrt(x) — квадратный корень

math.radians(x) — переводит градусы в радианы

math.degrees(x) — наоборот, радианы в градусы '''